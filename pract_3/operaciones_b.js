const n1 = null;
const n2 = null;

function suma(n1, n2) {
  return n1 + n2;
}

function resta(n1, n2) {
  return n1 - n2;
}

function multi(n1, n2) {
  return n1 * n2;
}

function div(n1, n2) {
  return n1 / n2;
}

module.exports.n1 = n1;
module.exports.n2 = n2;
module.exports.suma = suma;
module.exports.resta = resta;
module.exports.multi = multi;
module.exports.div = div;
